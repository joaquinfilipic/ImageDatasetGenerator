package ar.com.itba.ss.datasetgenerator.model.simulation;

public class DrivePoint {

	private double x;
	private double y;
	
	public DrivePoint(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}
	
}
