package ar.com.itba.ss.datasetgenerator.model.simulation;

public class GearVariables {
	
	private double r0;
	private double r1;
	private double r2;
	private double r3;
	private double r4;
	private double r5;
	
	public GearVariables(double r0, double r1, double r2, double r3, double r4, double r5) {
		this.r0 = r0;
		this.r1 = r1;
		this.r2 = r2;
		this.r3 = r3;
		this.r4 = r4;
		this.r5 = r5;
	}
	
	public double getR0() {
		return r0;
	}

	public void setR0(double r0) {
		this.r0 = r0;
	}

	public double getR1() {
		return r1;
	}

	public void setR1(double r1) {
		this.r1 = r1;
	}

	public double getR2() {
		return r2;
	}

	public void setR2(double r2) {
		this.r2 = r2;
	}

	public double getR3() {
		return r3;
	}

	public void setR3(double r3) {
		this.r3 = r3;
	}

	public double getR4() {
		return r4;
	}

	public void setR4(double r4) {
		this.r4 = r4;
	}

	public double getR5() {
		return r5;
	}

	public void setR5(double r5) {
		this.r5 = r5;
	}

}
